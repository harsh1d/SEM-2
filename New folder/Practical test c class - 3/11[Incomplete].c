// Write a program in C to print Floyd's Triangle.

#include<stdio.h>
int main()
{
 printf("\n HARSH D \n");
  int i,j,k,n;
  printf("Enter the number of rows: ");
  scanf("%d",&n);
  for(i=1;i<=n;i++)
    {
      for(j=1;j<=i;j++)
        {
          printf("%d ",k);
        }
      printf("\n");
    }
  return 0;
}
